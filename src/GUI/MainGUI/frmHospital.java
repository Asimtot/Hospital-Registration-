        jTable3 = new UpdatedTable<Patient>(new String[]{"Patient"},false,1) {
            @Override
            public String[][] createTable() {
                List<Patient> patientList = hospital.getAllPatients();
                setList(patientList);
                String[][] patientTable = new String[patientList.size()][2];
                for (int i = 0; i < patientTable.length; i++) {
                    patientTable[i][0] = patientList.get(i).getName();
                }
                return patientTable;
            }
        };

        jTable1 = new UpdatedTable<Consultation>(new String[]{"Date/Time", "Doctor", "Patient"}, false, 3) {
            @Override
            public String[][] createTable() {
                ArrayList<Consultation> consultationList = new ArrayList<>();
                for(Patient patient : hospital.getAllPatients()){
                    if (patient.getLastConsultation() != null )
                        consultationList.add(patient.getLastConsultation());
                }
                Collections.sort(consultationList);
                setList(consultationList);
                String[][] consultationTable = new String[consultationList.size()][3];
                for (int i = 0; i < consultationTable.length; i++) { //FIXME
//                    consultationTable[i][0] = consultationList.get(i).getDate().format(dateTimeFormatter);
//                    consultationTable[i][1] = consultationList.get(i).getDoctor().getName();
//                    consultationTable[i][2] = consultationList.get(i).getGeneralInfo().getPatient().getName();
                }
                return consultationTable;
            }
        };

        jTable2 = new UpdatedTable<Doctor>(new String[]{"Doctor Name", "Department"}, false, 2) {
            @Override
            public String[][] createTable() {
                List<Doctor> doctorList = hospital.getHospitalDoctors();
                setList(doctorList);
                String[][] doctorTable = new String[doctorList.size()][2];
                for (int i = 0; i < doctorTable.length; i++) {
                    doctorTable[i][0] = doctorList.get(i).getName();
                    doctorTable[i][1] = doctorList.get(i).getDepartment().getDepartmentName();
                }
                return doctorTable;
            }
        };

