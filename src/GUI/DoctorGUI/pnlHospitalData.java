    private void componentInitializer(){
        jTable1 = new UpdatedTable<Department>(new String[]{}, false, 1) {
            @Override
            public String[][] createTable() {
                List<Department> departmentList = hospital.getDepartments();
                String[] departmentNames = new String[departmentList.size()];
                for (int i = 0; i < departmentNames.length; i++) {
                    departmentNames[i] = departmentList.get(i).getDepartmentName();
                }
                setHeaders(departmentNames);
                setEditable(false, departmentList.size());
                String[][] departmentTable = new String[50][departmentList.size()];
                for (int i = 0; i < departmentTable[i].length; i++) {
                    List<Doctor> departmentDoctorList = departmentList.get(i).getDepartmentDoctors();
                    for (int j = 0; j < departmentDoctorList.size(); j++) {
                        departmentTable[j][i] = departmentDoctorList.get(j).getName();
                    }
                }
                return departmentTable;
            }
        };
    }

