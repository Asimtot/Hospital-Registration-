        jTable5 = new UpdatedTable<Department>(new String[]{"Department Name", "Doctor Count"}, false, 2) {
            @Override
            public String[][] createTable() {
                List<Department> departmentList = hospital.getDepartments();
                setList(departmentList);
                String[][] departmentTable = new String[departmentList.size()][2];
                for (int i = 0; i < departmentTable.length; i++) {
                    departmentTable[i][0] = departmentList.get(i).getDepartmentName();
                    departmentTable[i][1] = String.valueOf(departmentList.get(i).getDoctorNumber());
                }
                return departmentTable;
            }
        };

